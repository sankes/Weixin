public class LocationLatLng {

	private String lat;// 纬度, 40.055
	private String lng;// 经度,116.308

	public String getLat() {
		return lat;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}

	public String getLng() {
		return lng;
	}

	public void setLng(String lng) {
		this.lng = lng;
	}
}
